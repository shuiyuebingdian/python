'''
1-7-2: python中的等于
python中一个=表示赋值符，两个=表示数学中的等于
'''

# 定义一个姓名变量
name = "张三"
# 定义一个年龄变量
age = 10

# 打印变量name的值
print("name =", name)

# 打印变量age的值
print("age =", age)

# 打印变量age是否等于10
print("age等于10吗？", age == 10)

# 打印变量age是否不等于10
print("age不等于10吗？", age != 10)

# 打印变量age是否大于等于10
print("age大于等于10吗？", age >= 10)

# 打印变量age是否小于等于10
print("age小于等于10吗？", age <= 10)

# 打印变量age是否大于10
print("age大于10吗？", age > 10)

# 打印变量age是否小于10
print("age小于10吗？", age < 10)

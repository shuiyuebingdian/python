'''
使用input()函数获取键盘输入（键盘输入的内容都为字符串）
使用int()函数将输入的字符串转换成整数
使用input()函数输出带占位符的字符串
'''

# 命名两个变量，接收键盘输入的数字
a = int(input("请输入你要计算的第一个数： "))
b = int(input("请输入你要计算的第二个数： "))

# 打印加法等式
print('%d + %d = %d' % (a, b, a + b))

# 打印减法等式
print('%d - %d = %d' % (a, b, a - b))

# 打印乘法等式
print('%d * %d = %d' % (a, b, a * b))

# 打印除法等式，结果输出小数
print('%d / %d = %s' % (a, b, str(a / b)))


# 打印整除等式，结果输出商
print('%d // %d = %d' % (a, b, a // b))